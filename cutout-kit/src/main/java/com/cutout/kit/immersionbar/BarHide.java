public enum BarHide {
    /**
     * 隐藏状态栏
     * Flag hide status bar bar hide.
     */
    FLAG_HIDE_STATUS_BAR,
    /**
     * 隐藏导航栏
     * Flag hide navigation bar bar hide.
     */
    FLAG_HIDE_NAVIGATION_BAR,
    /**
     * 隐藏状态栏和导航栏
     * Flag hide bar bar hide.
     */
    FLAG_HIDE_BAR,
    /**
     * 显示状态栏和导航栏
     * Flag show bar bar hide.
     */
    FLAG_SHOW_BAR
}
